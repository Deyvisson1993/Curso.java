package ModeloUsuario;

public class Semestre {
	
	private String numero_semestre;
	private String ano_semestre;
	
	public String getNumero_semestre() {
		return numero_semestre;
	}
	public void setNumero_semestre(String numero_semestre) {
		this.numero_semestre = numero_semestre;
	}
	public String getAno_semestre() {
		return ano_semestre;
	}
	public void setAno_semestre(String ano_semestre) {
		this.ano_semestre = ano_semestre;
	}
	

}
