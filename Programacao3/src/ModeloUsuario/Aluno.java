package ModeloUsuario;

public class Aluno {
	
	private int cod_aluno;
	private String nome_aluno;
	private String turno_aluno;
	private String sexo_aluno;
	private String idade_aluno;
		
	
	public int getCod_aluno() {
		return cod_aluno;
	}
	public void setCod_aluno(int cod_aluno) {
		this.cod_aluno = cod_aluno;
	}
	public String getNome_aluno() {
		return nome_aluno;
	}
	public void setNome_aluno(String nome_aluno) {
		this.nome_aluno = nome_aluno;
	}
	public String getTurno_aluno() {
		return turno_aluno;
	}
	public void setTurno_aluno(String turno_aluno) {
		this.turno_aluno = turno_aluno;
	}
	public String getSexo_aluno() {
		return sexo_aluno;
	}
	public void setSexo_aluno(String sexo_aluno) {
		this.sexo_aluno = sexo_aluno;
	}
	public String getIdade_aluno() {
		return idade_aluno;
	}
	public void setIdade_aluno(String idade_aluno) {
		this.idade_aluno = idade_aluno;
	}


}
