package ModeloUsuario;

public class Professor {
	
	private String nome_prof;
	private String cpf_prof;
	private String turno_prof;
	private String especialização;
	private String sexo_prof;
	
	
	
	public String getNome_prof() {
		return nome_prof;
	}
	public void setNome_prof(String nome_prof) {
		this.nome_prof = nome_prof;
	}
	public String getCpf_prof() {
		return cpf_prof;
	}
	public void setCpf_prof(String cpf_prof) {
		this.cpf_prof = cpf_prof;
	}
	public String getTurno_prof() {
		return turno_prof;
	}
	public void setTurno_prof(String turno_prof) {
		this.turno_prof = turno_prof;
	}
	public String getEspecialização() {
		return especialização;
	}
	public void setEspecialização(String especialização) {
		this.especialização = especialização;
	}
	public String getSexo_prof() {
		return sexo_prof;
	}
	public void setSexo_prof(String sexo_prof) {
		this.sexo_prof = sexo_prof;
	}
	
	
}
